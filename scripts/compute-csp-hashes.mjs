// scripts/compute-csp-hashes.mjs
//
// Post-build step: scansiona gli HTML prerendered (SSG) alla ricerca di
// script inline, calcola SHA-256 base64 per ciascun body, e scrive il
// modulo TypeScript `src/generated/csp-hashes.ts` con la whitelist.
//
// Il modulo TS e poi importato da `src/middleware.ts`, che costruisce la
// direttiva `Content-Security-Policy` dinamicamente e la stampa come
// response header per ogni risposta text/html.
//
// Motivazione storica: originariamente iniettavamo gli hash direttamente
// in `.output/public/_headers`, ma Cloudflare Workers rifiuta linee
// > 2000 char in `_headers` (CF API policy [code: 10021]). Con 79 hash
// la direttiva superava i 4700 char. Spostare la CSP in runtime risolve.
//
// Pipeline di build (richiede doppio giro):
//   1. `vinxi build`               → middleware bundled con stub vuoto
//   2. `node scripts/compute-csp-hashes.mjs`
//                                  → rigenera src/generated/csp-hashes.ts
//   3. `vinxi build`               → middleware bundled con hash reali
//
// ref TASK-W18-T31, REQ-NF-018-002, OP-W18-002

import { readFile, writeFile, glob, mkdir } from 'node:fs/promises';
import { createHash } from 'node:crypto';
import { dirname } from 'node:path';

const HTML_GLOB = '.output/public/**/*.html';
const GENERATED_PATH = 'src/generated/csp-hashes.ts';

function sha256base64(content) {
  return createHash('sha256').update(content, 'utf8').digest('base64');
}

async function extractInlineScriptHashes() {
  const hashes = new Set();
  for await (const file of glob(HTML_GLOB)) {
    const html = await readFile(file, 'utf8');
    // Match <script ...> senza attributo src=...
    // Il gruppo catturato 1 e il body dello script (contenuto inline)
    const matches = html.matchAll(
      /<script(?![^>]*\ssrc=)[^>]*>([\s\S]*?)<\/script>/g,
    );
    for (const m of matches) {
      const body = m[1];
      if (body.trim().length > 0) {
        hashes.add(`sha256-${sha256base64(body)}`);
      }
    }
  }
  return Array.from(hashes).sort();
}

function renderModule(hashes) {
  const header = [
    '// AUTO-GENERATED by scripts/compute-csp-hashes.mjs. DO NOT EDIT MANUALLY.',
    '//',
    '// Contiene la whitelist degli hash SHA-256 (base64) degli script inline',
    '// emessi negli HTML prerendered (.output/public/**/*.html). Il middleware',
    '// (`src/middleware.ts`) importa questa costante per costruire la direttiva',
    '// `script-src` della response header CSP al runtime.',
    '//',
    '// Motivazione: Cloudflare Workers `_headers` rifiuta linee > 2000 char.',
    '// Con 79 hash (~4746 char) superavamo il limit → CSP viene emesso dal',
    '// Worker middleware invece che dal file statico.',
    '//',
    '// Pipeline:',
    '//   1. `vinxi build` (primo giro) usa lo stub vuoto committato (questo file',
    '//      quando il repo e fresh).',
    '//   2. `compute-csp-hashes.mjs` scansiona .output/public/**/*.html e',
    '//      sovrascrive questo file con gli hash reali.',
    '//   3. `vinxi build` (secondo giro) ricompila il middleware embeddando gli',
    '//      hash reali nel bundle .output/server.',
    '//',
    '// ref TASK-W18-T31, REQ-NF-018-002, OP-W18-002',
    '',
  ].join('\n');

  const body =
    hashes.length === 0
      ? 'export const INLINE_SCRIPT_HASHES: readonly string[] = [] as const;\n'
      : [
          'export const INLINE_SCRIPT_HASHES: readonly string[] = [',
          ...hashes.map((h) => `  '${h}',`),
          '] as const;',
          '',
        ].join('\n');

  return `${header}\n${body}`;
}

async function writeGeneratedModule(hashes) {
  await mkdir(dirname(GENERATED_PATH), { recursive: true });
  await writeFile(GENERATED_PATH, renderModule(hashes), 'utf8');
  console.log(
    `[csp] wrote ${hashes.length} inline script hashes → ${GENERATED_PATH}`,
  );
}

const hashes = await extractInlineScriptHashes();
await writeGeneratedModule(hashes);
if (hashes.length === 0) {
  console.warn(
    '[csp] WARNING: no inline scripts detected in .output/public — ' +
      'generated empty whitelist. Check prerender output.',
  );
}
